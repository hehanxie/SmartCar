void LCD_Fill_Line(byte line,byte bmp_data)   //自加填充整行亮或暗
{
  LCD_WrCmd(0xb0+line);
  LCD_WrCmd(0x01);
  LCD_WrCmd(0x10);
  for(byte x=0;x<X_WIDTH;x++)
    LCD_WrDat(bmp_data);
}
